        <h2 id="seccion3" class="blog-post-title mb-1">Aves</h2>
        <p class="blog-post-meta">January 1, 2021 by <a href="https://enciclovida.mx/especies/22655-aves" target="_blank">CONABIO</a></p>

        <p>Acerca de...</p>
        <hr>
        <p>Las aves son vertebrados que se caracterizan principalmente por sus adaptaciones al vuelo.  Sus huesos son huecos y modificados, 
          lo que es más evidente en sus extremidades anteriores, en las que se fusionan formando las alas. Su cuerpo está cubierto de plumas, 
          son endotérmicos (mantienen su temperatura constante mediante el metabolismo) y presentan sacos de aire internos ubicados en la 
          zona del abdomen. Estos animales comen constantemente para mantener su alto metabolismo necesario para el vuelo. Su lengua está 
          modificada según su alimento, puesto que todas las aves carecen de dientes (Miller y Harley, 1996).</p>
        <br>

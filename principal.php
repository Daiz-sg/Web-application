        <h2 id="seccion2" class="blog-post-title mb-1">Mamíferos</h2>
        <p class="blog-post-meta">January 1, 2021 by <a href="https://enciclovida.mx/especies/22653-mammalia" target="_blank">CONABIO</a></p>

        <p>Acerca de...</p>
        <hr>
        <p>Los <b>mamíferos</b> son el grupo más conocido de los vertebrados. Las hembras tienen glándulas mamarias con las que alimentan a sus crías. 
          La mayoría da a luz crías vivas (solo el <b>ornitorrinco</b> y los <b>equidnas</b> ponen huevos) y, salvo algunas excepciones, tiene su cuerpo 
          cubierto de pelo. Debido a que han desarrollado una serie de formas para desplazarse, se han diseminado y diversificado ampliamente 
          en diversos hábitats (tierra, aire y agua). Su éxito se debe, en gran medida, a su habilidad para mantener una temperatura corporal 
          constante, a pesar de los cambios en las condiciones externas.</p>
        <br>
        <div class="col">
        <div class="card card-cover h-100 overflow-hidden text-bg-dark rounded-4 shadow-lg" style="background-image: url('img/mamife.jpg');">

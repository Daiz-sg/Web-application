        <h2 id="seccion4" class="blog-post-title mb-1">Reptiles</h2>
        <p class="blog-post-meta">January 1, 2021 by <a href="https://enciclovida.mx/especies/22647-reptilia" target="_blank">CONABIO</a></p>

        <p>Acerca de...</p>
        <hr>
        <p>Los reptiles son vertebrados con piel escamosa, seca, queratinizada y gruesa; esta piel es mudada periódicamente. Su respiración es 
          únicamente vía pulmonar (Burnie, 2003). Se clasifican en cuatro órdenes: Cocodrilos (Crocodylia), <b>Lagartijas y Serpientes</b> (Squamata), 
          <b>Tortugas</b> (Testudines) y <b>Tuátaras</b> (Sphenodontia). De estos cuatro, los tres primeros están representados en México. Existen 9,547 
          especies de reptiles en el mundo. En México se han descrito 1022 especies de reptiles  y se considera como el segundo país con 
          diversidad más alta de este grupo después de Australia. En México viven 417 lagartijas, 393 serpientes, 48 tortugas, 3 cecilias y 3 
          cocodrilos. Siete de las ocho tortugas marinas del mundo, viven y anidan en México. La tortuga lora (Lepidochelys kempi) solamente 
          anida en México.</p>

        <blockquote class="blockquote">
          <p>Algunas de sus características únicas son: </p>
        </blockquote>
        <ul>
          <li>Tienen un solo hueso en la mandíbula.</li>
          <li>Los huesos presentes en la mandíbula de los reptiles son ahora el martillo y el yunque, huesecillos del oído en los mamíferos.</li>
          <li>Tienen dientes con formas distintas (heterodoncia) para funciones diversas (incisivos, colmillos, caninos, premolares, molares). </li>
        </ul>
        <p>Se clasifican en tres grupos:</p>
        <ol>
          <li>Los monotremas (ornitorrinco y equidnas), los marsupiales (canguros y tlacuaches) y los placentarios.</li>
          <li>Los primeros ponen huevos, los segundos tienen un marsupio (bolsa externa) en donde las crías terminan su desarrollo.</li>
          <li>Los terceros nacen completamente desarrollados. Los grupos más diversos de mamíferos son los roedores y los murciélagos.</li>
        </ol>
